class Calculator
  # Constructor
  def initialize
  end

  # Public methods.
  # The last expresion evaluated is returned.
  def plus(num1, num2)
    num1 + num2
  end

  def diff(num1, num2)
    num1 - num2
  end

  def prod(num1, num2)
    num1 * num2
  end

  def div!(num1, num2)
      num1 / num2
  end
end

